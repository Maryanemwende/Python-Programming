#!/usr/bin/python3

#declaration and basic manipulation

#declare a list (cars)
best_cars = ['Mercedes', 'Rolls Royce', 'Toyota', 'Suzuki']

#lets print out the list
print(best_cars)

#say we want to access a particular car in the list(indexing)
print(best_cars[1])

#Negative indexing to access the list from the last element
print(best_cars[-1])
print(best_cars[-3])

#let's do some Slicing
print(best_cars[0:3]) #prints from index 0 upto 3, excluding 3
print(best_cars[1:2])
print(best_cars[1:3])
